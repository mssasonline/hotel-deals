// Language overrides map — only languages with translations are listed.
// All other Language codes automatically fall back to English.
const languageOverrides: Partial<Record<Language, Partial<Record<TranslationKey, string>>>> = {
  en,
  ar,
  fr,
  ru,
  de,
  'zh-cn': zhCn,
  tr,
};

export function getTranslations(lang: Language): Record<TranslationKey, string> {
  const base = en as Record<TranslationKey, string>;
  if (lang === 'en') return base;
  const overrides = languageOverrides[lang];
  if (!overrides) return base;
  return { ...base, ...overrides };
}
